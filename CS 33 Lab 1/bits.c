/* 
 * minusOne - return a value of -1 
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 2
 *   Rating: 1
 */
int minusOne(void) 
{
  int x;
  return (x + ~x); // taking the sum of any int x with its complement will always yield minus one
}
/*
 * isZero - returns 1 if x == 0, and 0 otherwise 
 *   Examples: isZero(5) = 0, isZero(0) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 2
 *   Rating: 1
 */
int isZero(int x) 
{
  return !x; // the ! operator returns 1 if x is 0 and returns 0 if x is not 0
}
/* 
 * getByte - Extract byte n from word x
 *   Bytes numbered from 0 (LSB) to 3 (MSB)
 *   Examples: getByte(0x12345678,1) = 0x56
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 2
 */
int getByte(int x, int n) 
{
  int shift = n << 3; // shifting left 3 is the magnitude of a byte 
  int shifted = x >> shift; // shifting n times
  return shifted & 0xFF; // & with 1...11 to get the entire byte
}
