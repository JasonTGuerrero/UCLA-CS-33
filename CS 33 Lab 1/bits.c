  int sum = x + y; // compute the sum 
  int x_sign = x >> 31; // get all the signs of x,y and the sum
  int y_sign = y >> 31;
  int sum_sign = sum >> 31;
  int a = ~(x_sign ^ y_sign); // check if x and y have same sign
